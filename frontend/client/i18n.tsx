interface I18nContextProps {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: string) => string;
  LANGUAGES: Record<Language, string>;
}

const I18nContext = createContext<I18nContextProps | null>(null);

export const LanguageProvider = ({ children }: { children: React.ReactNode }) => {
  const [lang, setLang] = useState<Language>(
    (localStorage.getItem("hooslearn_lang") as Language) || "en"
  );

  useEffect(() => {
    localStorage.setItem("hooslearn_lang", lang);
  }, [lang]);

  const value = useMemo(
    () => ({
      lang,
      setLang,
      LANGUAGES,
      t: (key: string) => {
        const keys = key.split(".");

        const resolve = (obj: any) => {
          let result = obj;
          for (const k of keys) {
            result = result?.[k];
            if (result === undefined) break;
          }
          return result;
        };

        const translated = resolve(TRANSLATIONS[lang]);
        if (typeof translated === "string") {
          return translated;
        }

        const fallback = resolve(TRANSLATIONS.en);
        if (typeof fallback === "string") {
          return fallback;
        }

        // if it may be a top-level key without dot path
        if (keys.length === 1) {
          const fallbackTop = TRANSLATIONS[lang][key];
          if (typeof fallbackTop === "string") return fallbackTop;
          const fallbackTopEnglish = TRANSLATIONS.en[key];
          if (typeof fallbackTopEnglish === "string") return fallbackTopEnglish;
        }

        return key;
      },
    }),
    [lang]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n must be used within LanguageProvider");
  }
  return ctx;
};
